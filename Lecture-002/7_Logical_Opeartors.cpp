#include <iostream>
using namespace std;

int main() {
    // Declare and initialize variables
    bool a = true;
    bool b = false;

    // Logical AND (&&)
    cout << "Logical AND (a && b): " << (a && b) << endl;

    // Logical OR (||)
    cout << "Logical OR (a || b): " << (a || b) << endl;

    // Logical NOT (!)
    cout << "Logical NOT (!a): " << (!a) << endl;
    cout << "Logical NOT (!b): " << (!b) << endl;

    return 0;
}

